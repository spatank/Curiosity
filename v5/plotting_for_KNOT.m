%% Load processed data

base_path = '/Volumes/My Passport/Curiosity/';
addpath(genpath(fullfile(base_path, 'Helper')))
data_path = '/Volumes/My Passport/Curiosity/v5/Data/KNOT/';
subj_ID = 115;
load(strcat(data_path, 'subj_', string(subj_ID), '_C_DoF.mat'))
